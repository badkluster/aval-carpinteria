import { ENV } from "../utils";

export function getDocumentsTypes(name) {
  const code = name;

  const url = `${ENV.BASE_API}/documentType?code=${code}`;

  const params = {
    method: "GET",
    headers: {
      "Content-Type": "application/json",
    },
  };

  return fetch(url, params)
    .then((response) => {
      return response.json();
    })
    .then((result) => {
      return result;
    })
    .catch((err) => {
      return err.message;
    });
}

